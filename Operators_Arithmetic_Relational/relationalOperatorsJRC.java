public class relationalOperatorsJRC{

    public static void main(String [] args){
        int x = 10;
        int y = 15;

        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x >= y);
        System.out.println(x <= y);
        
        // Bonus Task
        if(x >= 10 && x <= 50){
            System.out.println("Value of X: " + x + " " + "is Within the range of 10 upto 50");
        }



    }


}